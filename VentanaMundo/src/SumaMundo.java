
public class SumaMundo {

	private int sumando1=0;
	private int sumando2=0;
	public SumaMundo(int num1,int num2) {
		sumando1=num1;
		sumando2=num2;
	}
	
	public int suma()
	{
		return sumando1+sumando2;
	}
	public int resta()
	{
		return sumando1-sumando2;
	}
	
	public int multiplicacion()
	{
		return sumando1*sumando2;
	}
	
	

}
